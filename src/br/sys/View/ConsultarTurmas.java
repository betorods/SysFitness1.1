    public void pesquisaTurma() {
        String sql = "Select cod_turma,mat_Professor,capacidade,horario,turno,alunos_cadastrado FROM turma WHERE cod_turma LIKE ?";

        try {
            pst = conecta.prepareStatement(sql);
            pst.setString(1, codigoTurma.getText() + "%");

            rs = pst.executeQuery();
            TabelaTurma.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, error);
        }
    }


